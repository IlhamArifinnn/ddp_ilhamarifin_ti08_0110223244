import math

def tambah(bil1:int,bil2:int):
    hasil = bil1 + bil2
    return hasil

def kurang(bil1:int,bil2:int):
    hasil = bil1 - bil2
    return hasil

def kali(bil1:int,bil2:int):
    hasil = bil1 * bil2
    return hasil

def bagi(bil1:int,bil2:int):
    hasil = bil1 / bil2
    return hasil

def pangkat(bil1:int,bil2:int):
    hasil = bil1 ** bil2
    return hasil

def log(bil1:int):
    return math.log(bil1)

def akar(bil1:int):
    hasil =  math.sqrt(bil1)
    return hasil

def sin(bil1:int):
    hasil = math.sin(bil1)
    return hasil

def cos(bil1:int):
    hasil = math.cos(bil1)
    return hasil

def tan(bil1:int):
    return math.tan(bil1)

